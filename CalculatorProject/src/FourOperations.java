public class FourOperations {
    public int Sum(int num1, int num2) {
        return num1 + num2;
    }

    public int Minus(int num1, int num2) {
        return num1 - num2;
    }

    public int Times(int num1, int num2) {
        return num1 * num2;
    }

    public int Divide(int num1, int num2) {
        return num1 / num2;
    }
}
